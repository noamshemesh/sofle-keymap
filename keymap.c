bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case KC_QWERTY:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_QWERTY);
            }
            return false;
        /* KEYBOARD PET STATUS START */

        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        /* KEYBOARD PET STATUS END */

        case KC_TOG_BACKLIGHT:
            if (record->event.pressed) {
                rgblight_set_layer_state(4, !rgblight_get_layer_state(4));
            }
            break;
        case KC_TOG_KEYLIGHT:
            if (record->event.pressed) {
                rgblight_set_layer_state(5, !rgblight_get_layer_state(5));
            }
            break;
    }

    keyboard_wake_up();
    
    return true;
}

