void wpm_and_luna(void) {
    /* wpm counter */
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(0, 9);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);
    oled_set_cursor(0, 8);

    oled_write("wpm", false);

    render_luna(0, 13);
}

static void print_logo_narrow(void) {
    oled_set_cursor(0, 0);
    static char rgbStatusLine1[26] = {0};
    snprintf(rgbStatusLine1, sizeof(rgbStatusLine1), "M:%.2d", rgblight_get_mode());
    oled_write_ln(rgbStatusLine1, false);
    static char rgbStatusLine2[26] = {0};
    snprintf(rgbStatusLine2, sizeof(rgbStatusLine2), "h:%.3ds:%.3dv:%.3dd:%.3d", rgblight_get_hue(), rgblight_get_sat(), rgblight_get_val(), rgblight_get_speed());
    oled_write_ln(rgbStatusLine2, false);

    wpm_and_luna();
}

